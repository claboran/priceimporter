-- Region dimension


create COLUMN TABLE d_region (
  id BIGINT PRIMARY KEY NOT NULL,
  region NVARCHAR(50)
);
alter table d_region add CONSTRAINT region_un UNIQUE (region);
create SEQUENCE region_seq RESET BY SELECT IFNULL(MAX(id), 0) + 1 from d_region;

-- PeriodType dimension


create COLUMN TABLE d_period_type (
  id BIGINT PRIMARY KEY NOT NULL,
  period_type NVARCHAR(50)
);
alter table d_period_type add CONSTRAINT period_un UNIQUE (period_type);
create SEQUENCE period_type_seq RESET BY SELECT IFNULL(MAX(id), 0) + 1 from d_period_type;

-- Time date dimension


create COLUMN TABLE d_date_time (
  id BIGINT PRIMARY KEY NOT NULL,
  day_of_month INTEGER,
  month_of_year INTEGER,
  the_year INTEGER,
  hour_of_day INTEGER,
  minutes_of_hour INTEGER
);
alter table d_date_time add CONSTRAINT date_time_un UNIQUE (day_of_month, month_of_year, the_year, hour_of_day, minutes_of_hour);
create SEQUENCE date_time_seq RESET BY SELECT IFNULL(MAX(id), 0) + 1 from d_date_time;

-- Fact table


create COLUMN TABLE f_energy_price_demand (
  id BIGINT PRIMARY KEY NOT NULL,
  total_Demand DECIMAL(20,2),
  rpr DECIMAL(20,2),
  d_region_id BIGINT NOT NULL,
  d_period_type_id BIGINT NOT NULL,
  d_date_time_id BIGINT NOT NULL,
  FOREIGN KEY (d_region_id) REFERENCES d_region(id),
  FOREIGN KEY (d_period_type_id) REFERENCES d_period_type(id),
  FOREIGN KEY (d_date_time_id) REFERENCES d_date_time(id)
);
create SEQUENCE fact_seq RESET BY SELECT IFNULL(MAX(id), 0) + 1 from f_energy_price_demand;

